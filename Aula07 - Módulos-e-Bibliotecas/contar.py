def caracteres(palavra):
    """
    Conta quantos caracteres existem em uma palavra.
    :param palavra: string
    :return: inteiro (número de caracteres)
    """
    return len(palavra)


def numeros(n):
    """
    Retorna o número dado multiplicado por ele mesmo (ao quadrado).
    :param n: número inteiro ou float
    :return: n²
    """
    return n * n


def somarNumeros(num1, num2):
    return f'A soma é: {num1 + num2}'